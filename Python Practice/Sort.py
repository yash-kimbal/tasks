# Sorting a list
ol = [4, 2, 8, 1, 6]
sl = sorted(ol)
print("Original List:", ol)
print("Sorted List:", sl)

# Sorting a tuple
ot = (10, 5, 8, 3)
sorted_tuple = tuple(sorted(ot))
print("Original Tuple:", ot)
print("Sorted Tuple:", sorted_tuple)

# Sorting a string
os = "python"
sorted_string = ''.join(sorted(os))
print("Original String:", os)
print("Sorted String:", sorted_string)
